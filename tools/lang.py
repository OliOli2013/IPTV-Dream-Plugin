def normalize_lang(lang, default='en'):
    try:
        l = str(lang or '').strip().lower()[:2]
    except Exception:
        l = ''
    return l if l in SUPPORTED_LANGS else default

def _(key, lang='en'):
    l = normalize_lang(lang, 'en')
    return LANG.get(l, LANG['en']).get(key, LANG['en'].get(key, key))

